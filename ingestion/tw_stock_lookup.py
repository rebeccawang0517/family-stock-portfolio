def _norm(s: str) -> str:
    if not s:
        return ''
    # 全形數字 → 半形
    s = s.translate(str.maketrans('０１２３４５６７８９', '0123456789'))
    return re.sub(r'\s+', '', s)


# 反查表：name → ticker（在 module 載入時建一次）
_NAME_TO_TICKER: dict[str, str] = {
    _norm(name): ticker
    for ticker, name in TAIWAN_STOCKS_TICKER_TO_NAME.items()
}


def resolve_ticker(pdf_name: str) -> str | None:
    """中文簡稱 → 股票代號。找不到回 None。"""
    if not pdf_name:
        return None
    name = _norm(pdf_name)

    # 1. 直接覆蓋
    if name in OVERRIDES:
        return OVERRIDES[name]

    # 2. 完全相等
    if name in _NAME_TO_TICKER:
        return _NAME_TO_TICKER[name]

    # 3. fuzzy：PDF name 以標準名開頭 (e.g., 緯穎科技 startswith 緯穎)
    #    ※ 不能用 in：「群聯電子」包含「聯電」會誤判成 2303
    candidates: list[tuple[str, str]] = []
    for std_name, ticker in _NAME_TO_TICKER.items():
        if name.startswith(std_name):
            candidates.append((std_name, ticker))
    if candidates:
        # 取最長相符的
        candidates.sort(key=lambda x: len(x[0]), reverse=True)
        return candidates[0][1]

    # 4. fuzzy：標準名 contains PDF name（PDF name 比較短）
    for std_name, ticker in _NAME_TO_TICKER.items():
        if name in std_name:
            return ticker

    return None
